# Operators on Numeric Data

# + (Addition)
# - (Subtraction)
# * (Multiplication)
# / (Division)
# % (Modulo - returns the remainder of a division)
# ** (Exponentiation - raises a number to a power)
# // (Floor Division - returns the integer quotient of a division)

x = 10
y = 2

print("Addition:", x + y)         # 10 + 2 = 12
print("Subtraction:", x - y)      # 10 - 2 = 8
print("Multiplication:", x * y)   # 10 * 2 = 20
print("Division:", x / y)         # 10 / 2 = 5.0
print("Modulo:", x % y)           # 10 % 2 = 0
print("Exponentiation:", x ** y)  # 10 ** 2 = 100
print("Floor Division:", x // y)  # 10 // 2 = 5

